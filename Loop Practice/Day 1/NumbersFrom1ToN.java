
public class NumbersFrom1ToN {
    public static void numbers() {
        int num = 10;

        for(int a = 0; a < num; a++) {
            System.out.print(a + " ");
        }
        System.out.println();
    }

    public static void natural() {
        int num = 10;

        for(int a = 0; a <= num; a++) {
            System.out.print(a + " ");
        }
        System.out.println();
    }
}
